DELETE 
FROM geonames.featureCodes as fc
WHERE fc.code in (
    SELECT DISTINCT
        fcode
    FROM (
            SELECT
                f.code as fcode,
                g.geonameid as geonameid
            FROM
                geonames.featureCodes as f
            LEFT JOIN
                geonames.geoname as g
            on
                CONCAT(g.fclass,'.',g.fcode) = f.code
          ) as temp0
            WHERE temp0.geonameid is null
);
    
